# >, <,!=, ==, <=, >=

# > checks if the left side of equation is bigger than the right side.

print(80>70) # -> True

# < checks if the left side of equation is less than the right side.
print(80<70)  # False

print(int(70>40)) # 1

# == sign will check if both side of the equation are the same.

print(6==6) # True

#<= checks if the left side of equation is less than or equal the right side.

print(6<=6)  #True

print(False<=True) #True

print(1==True) # True

print(False==0) #True

print(False>-2) #True
